import java.util.*;

public class casting {
    public static void main(String[] args){
        //casting
        // implicit casting
        double price = 100.00;
        double finalprice = price + 18;
        System.out.println(finalprice);
        //Explicit casting
        int p=100;
        int fp= p + (int)18.9;
        System.out.println(fp);
        
    }
}
